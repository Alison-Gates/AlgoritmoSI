teste1 = input('Digite qualquer coisa para testar isnumeric: ')
print(teste1.isnumeric()) #verifica se é numérico

teste2 = input('Digite qualquer coisa para testar isalpha: ')
print(teste2.isalpha()) #verifica se é alfabético

teste3 = input('Digite qualquer coisa para testar isalnum: ')
print(teste3.isalnum()) #verifica se é alfanumérico

teste4 = input('Digite qualquer coisa para testar isupper: ')
print(teste4.isupper()) #verifica se está com letra maiuscula 

teste5 = input('Digite qualquer coisa para testar islower: ')
print(teste5.islower()) #verifica se está com letra minúscula 

teste6 = input('Digite qualquer coisa para testar isspace: ')
print(teste6.isspace()) #verifica se contém somente espacos

teste7 = input('Digite qualquer coisa para testar istitle: ')
print(teste7.istitle())